</p>
<ul>
  <li>includeva <strong>due provvigioni</strong> – una per ciascuna parte, con il primo "intermediario" che in realtà non intermediava nulla,</li>
  <li>non era chiaro <strong>cosa si stesse realmente pagando</strong>,</li>
  <li><strong>la provvigione doveva essere pagata al momento della prenotazione</strong>, quasi il 30% del prezzo dell’immobile,</li>
  <li>nel contratto non c’era alcuna garanzia per me come acquirente – solo che <strong>loro non avrebbero perso la provvigione</strong>, e io sì la mia caparra se cambiavo idea.</li>
</ul>
<p>
  L’immobile l’ho trovato <strong>da solo</strong> – su un portale di annunci italiano dove pubblicano sia agenzie che privati. Quando ho mostrato questo contratto al mio amico Honza – avvocato – la sua reazione è stata chiara:
</p>
<blockquote>
  «Se firmi questo, non chiedermi mai più un consiglio legale. Questo non è un contratto, è un diktat.»
</blockquote>
<p>
